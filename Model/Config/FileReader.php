declare(strict_types=1);

namespace AuroraExtensions\NotificationOutbox\Model\Config;

use Magento\Framework\{
    Config\ConverterInterface,
    Config\Dom,
    Config\FileResolverInterface,
    Config\Reader\Filesystem,
    Config\SchemaLocatorInterface,
    Config\ValidationStateInterface
};

class FileReader extends Filesystem
{
    /** @constant string XML_FILE */
    public const XML_FILE = 'notifications.xml';

    /** @property array $_idAttributes */
    protected $_idAttributes = [
        '/config/release' => 'version',
        '/config/release/notifications/notification' => 'index',
    ];

    /**
     * @param FileResolverInterface $fileResolver
     * @param ConverterInterface $converter
     * @param SchemaLocatorInterface $schemaLocator
     * @param ValidationStateInterface $validationState
     * @param string $fileName
     * @param array $idAttributes
     * @param string $domDocumentClass
     * @param string $defaultScope
     * @return void
     */
    public function __construct(
        FileResolverInterface $fileResolver,
        ConverterInterface $converter,
        SchemaLocatorInterface $schemaLocator,
        ValidationStateInterface $validationState,
        $fileName = self::XML_FILE,
        $idAttributes = [],
        $domDocumentClass = Dom::class,
        $defaultScope = 'global'
    ) {
        parent::__construct(
            $fileResolver,
            $converter,
            $schemaLocator,
            $validationState,
            $fileName,
            $idAttributes,
            $domDocumentClass,
            $defaultScope
        );
    }
}
